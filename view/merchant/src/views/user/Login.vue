    // this.requiredTwoStepCaptcha = true
  },
  methods: {
    // ...mapActions(['Login', 'Logout']),
    // handler
    handleUsernameOrEmail (rule, value, callback) {
      const { state } = this
      const regex = /^([a-zA-Z0-9_-])+@([a-zA-Z0-9_-])+((\.[a-zA-Z0-9_-]{2,3}){1,2})$/
      if (regex.test(value)) {
        state.loginType = 0
      } else {
        state.loginType = 1
      }
      callback()
    },
    handleTabClick (key) {
      this.customActiveKey = key
      // this.form.resetFields()
    },
    handleSubmit (e) {
      // this.$router.push({ path: '/' })
      e.preventDefault()
      const {
        form: { validateFields },
        state,
        customActiveKey,
        Login
      } = this
      console.log(Login)

      state.loginBtn = true

      const validateFieldsKey = customActiveKey === 'tab1' ? ['username', 'password'] : ['mobile', 'captcha']

      validateFields(validateFieldsKey, { force: true }, (err, values) => {
        if (!err) {
          console.log('login form', values)
          const loginParams = { ...values }
          delete loginParams.username
          loginParams['account'] = values.username
          loginParams.password = values.password
          console.log(loginParams)
          this.loginPost(loginParams)
        } else {
          setTimeout(() => {
            state.loginBtn = false
          }, 600)
        }
      })
    },
    loginPost (values) {
      console.log(values)
      Login(values).then((res) => {
        console.log(res)
        if (res.success) {
          const url = 'http://kefu.xuandev.com/'
          // 微擎
          // const url = `${window.location.host}/web`
          storage.set(ACCESS_TOKEN, res.result.token, 7 * 24 * 60 * 60 * 1000)
          this.$store.commit('SET_TOKEN', res.result.token)
          sessionStorage.setItem('UserToken', res.result.token)
          sessionStorage.setItem('UserId', res.result.id)
          sessionStorage.setItem('userName', res.result.name)
          sessionStorage.setItem('userUrl', url)
          this.$store.commit('changeUrl', sessionStorage.getItem('userUrl'))
          this.$store.commit('updateName', res.result.name)
          // this.$store.commit('SET_TOKEN', res.result.name)
          // console.log(storage.get(ACCESS_TOKEN))
          this.loginSuccess('loginSuccess')
        } else {
          this.isLoginError = true
          this.LoginErrorMsg = res.error.message
          this.state.loginBtn = false
        }
      })
    },
    loginSuccess (res) {
      console.log(res)
      this.$router.push({ path: '/' })
      console.log(this.$router)
      // 延迟 1 秒显示欢迎信息
      setTimeout(() => {
        this.$notification.success({
          message: '欢迎',
          description: `${timeFix()}，欢迎回来`
        })
      }, 1000)
      this.isLoginError = false
    },
    requestFailed (err) {
      this.isLoginError = true
      this.$notification['error']({
        message: '错误',
        description: ((err.response || {}).data || {}).message || '请求出现错误，请稍后再试',
        duration: 4
      })
    }
  }
}
